#include "3-calc.h"

/**
 * op_add - sum of 2 integers
 * @a: int 1
 * @b: int 2
 * 
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts 1 int from another
 * @a: int 1
 * @b: int 2
 * 
 * Return: sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - mult 2 integers
 * @a: int 1
 * @b: int 2
 * 
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - div 2 integers
 * @a: int 1
 * @b: int 2
 * 
 * Return: div
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - mod of 2 integers
 * @a: int 1
 * @b: int 2
 * 
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}



